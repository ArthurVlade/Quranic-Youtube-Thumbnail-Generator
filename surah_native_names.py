def arabic_surah_name(surah: Surah) -> str:
    """Return the surah name portion from the Arabic field (without leading سورة)."""
    return surah.arabic.replace("سورة ", "").strip()


def uses_native_script(code: str) -> bool:
    return code in NATIVE_SCRIPT_LANGS


def native_surah_name(code: str, number: int, surah: Surah, prefix: str, english_short: str) -> str:
    """Build the localized surah title shown on thumbnails."""
    if code == "zh":
        return f"第{number}章 {NAMES_ZH[number]}"
    if code == "ja":
        return f"{prefix} {NAMES_JA[number]}"
    if code == "ko":
        return f"{prefix} {NAMES_KO[number]}"
    if code in {"ar", "ur", "fa", "hi", "bn"}:
        return f"{prefix} {arabic_surah_name(surah)}"
    return f"{prefix} {english_short}"
