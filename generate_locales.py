def _english_short(english: str) -> str:
    return english.replace("Surah ", "").strip()


def _surah_title(code: str, prefix: str, english: str, number: int) -> str:
    from surah_native_names import native_surah_name
    from surahs import get_surah

    surah = get_surah(number)
    if not surah:
        return f"{prefix} {_english_short(english)}"
    return native_surah_name(code, number, surah, prefix, _english_short(english))


def build_locale(code: str, name_en: str, native: str, prefix: str) -> dict:
    ui = dict(UI_EN)
    ui.update(UI_TRANSLATIONS.get(code, {}))
    ui.update(EXTENDED_UI.get(code, {}))
    surahs = {
        str(s.number): _surah_title(code, prefix, s.english, s.number)
        for s in SURAHS
    }
    reciters = dict(RECITER_EN)
    reciters.update(RECITER_TRANSLATIONS.get(code, {}))
    categories = dict(CATEGORIES_EN)
    categories.update(CATEGORY_TRANSLATIONS.get(code, {}))
    return {
        "meta": {"code": code, "name_en": name_en, "name_native": native},
        "ui": ui,
        "surahs": surahs,
        "reciters": reciters,
        "categories": categories,
    }


def main() -> None:
    OUT.mkdir(parents=True, exist_ok=True)
    for code, name_en, native, prefix in LANGUAGES:
        data = build_locale(code, name_en, native, prefix)
        path = OUT / f"{code}.json"
        path.write_text(json.dumps(data, ensure_ascii=False, indent=2), encoding="utf-8")
        print(path.name)
    print(f"Generated {len(LANGUAGES)} locales in {OUT}")


if __name__ == "__main__":
    main()
